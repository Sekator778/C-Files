#include "../includes/ft.h"

int	sum(int a, int b)
{
	return (a + b);
}

int	div(int a, int b)
{
	return (a / b);
}

int	mult(int a, int b)
{
	return (a * b);
}

int	sub(int a, int b)
{
	return (a - b);
}

int	mod(int a, int b)
{
	return (a % b);
}
